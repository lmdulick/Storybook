    private void yourGuessTxtFieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_yourGuessTxtFieldActionPerformed
        RandomObject o = new RandomObject();
      
        String name = passedName.getText();
        String show = passedShow.getText();
        String subject = passedSubject.getText();
        String age = passedAge.getText();
        Person p = new Person(name, show, subject, age);
        String nameCompare = passedCharacterName.getText();
                
        if(yourGuessTxtField.getText().isEmpty()){
            JOptionPane.showMessageDialog(yourGuessTxtField, "Please enter a guess");
        }else if(yourGuessTxtField.getText().equalsIgnoreCase(nameCompare)){
            JOptionPane.showMessageDialog(yourGuessTxtField, "Your answer is correct!");
            new Library2(p).setVisible(true);
            this.setVisible(false);
        }else{
            yourGuessTxtField.setText("");
            JOptionPane.showMessageDialog(yourGuessTxtField, "Your answer is incorrect! Try again");
            
            if(yourGuessTxtField.getText().equalsIgnoreCase(nameCompare)){
            JOptionPane.showMessageDialog(yourGuessTxtField, "Your answer is correct!");
            new Library2(p).setVisible(true);
            this.setVisible(false);
        }
        }
    }//GEN-LAST:event_yourGuessTxtFieldActionPerformed

