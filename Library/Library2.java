    private void txtField1ActionPerformed(java.awt.event.ActionEvent evt) {                                          
        String name = passedName.getText();
        String show = passedShow.getText();
        String subject = passedSubject.getText();
        String age = passedAge.getText();
        Person p = new Person(name, show, subject, age);
        
        if(txtField1.getText().isEmpty()){
            JOptionPane.showMessageDialog(txtField1, "Please enter a guess");
        }else if(txtField1.getText().equalsIgnoreCase("Full House")){
            JOptionPane.showMessageDialog(txtField1, "Your answer is correct!");          
        }else{
            JOptionPane.showMessageDialog(txtField1, "Your answer is incorrect. Try again!"
                    + "\nHINT: DJ Tanner");
            txtField1.setText("");
            
            if(txtField1.getText().equalsIgnoreCase("Full House")){
                JOptionPane.showMessageDialog(txtField1, "Your answer is correct!");
            }
        }
    }
        
